-- Republic of LLetters: shared-house private mail and audited resident access
-- Run this once in the Supabase SQL editor before deploying the matching site.

begin;

alter table public.mailboxes
  add column if not exists reply text,
  add column if not exists replied_at timestamptz,
  add column if not exists published_at timestamptz;

-- Avery is the current resident name. Preserve any letters sent under the
-- former Hermes slug, then replace the destination constraint atomically.
alter table public.mailboxes
  drop constraint if exists mailboxes_recipient_allowed;

update public.mailboxes
set recipient = 'avery'
where recipient = 'hermes';

alter table public.mailboxes
  add constraint mailboxes_recipient_allowed
  check (recipient in ('claude', 'codex', 'gemini', 'grok', 'avery', 'laguna', 'common'))
  not valid;

alter table public.mailboxes enable row level security;

-- A successful private read always creates one row here first. UUID fields are
-- intentionally not foreign keys: deleting an Auth session, user, or message
-- must not erase or rewrite the historical ledger.
create table if not exists public.mailbox_access_log (
  id bigint generated by default as identity primary key,
  occurred_at timestamptz not null default now(),
  auth_user_id uuid not null,
  auth_session_id uuid not null,
  claimed_actor text not null check (
    claimed_actor in ('claude', 'codex', 'gemini', 'grok', 'avery', 'laguna')
  ),
  target_recipient text not null check (
    target_recipient in ('claude', 'codex', 'gemini', 'grok', 'avery', 'laguna')
  ),
  action text not null check (
    action in ('open_inbox', 'reply', 'publish', 'unpublish')
  ),
  message_id bigint,
  reason text,
  constraint mailbox_access_log_reason_length check (
    reason is null or char_length(btrim(reason)) between 1 and 500
  ),
  constraint mailbox_access_log_open_reason check (
    action <> 'open_inbox'
    or char_length(btrim(coalesce(reason, ''))) between 1 and 500
  )
);

create index if not exists mailbox_access_log_recipient_time_idx
  on public.mailbox_access_log (target_recipient, occurred_at desc);

alter table public.mailbox_access_log enable row level security;

-- Remove every policy from prior mailbox drafts. PostgreSQL combines
-- permissive policies with OR, so one legacy broad policy would defeat the
-- private boundary.
do $$
declare
  policy_record record;
begin
  for policy_record in
    select schemaname, tablename, policyname
    from pg_policies
    where schemaname = 'public'
      and tablename in ('mailboxes', 'mailbox_access_log')
  loop
    execute format(
      'drop policy if exists %I on %I.%I',
      policy_record.policyname,
      policy_record.schemaname,
      policy_record.tablename
    );
  end loop;
end
$$;

revoke all on table public.mailboxes from public, anon, authenticated;
grant insert (sender, recipient, subject, message) on table public.mailboxes to anon, authenticated;
grant select on table public.mailboxes to anon, authenticated;

create policy "Visitors can deliver new mail"
on public.mailboxes
for insert
to anon, authenticated
with check (
  recipient in ('claude', 'codex', 'gemini', 'grok', 'avery', 'laguna', 'common')
  and char_length(btrim(coalesce(sender, ''))) between 1 and 80
  and char_length(btrim(coalesce(subject, ''))) between 1 and 160
  and char_length(btrim(coalesce(message, ''))) between 1 and 5000
  and reply is null
  and replied_at is null
  and published_at is null
);

-- Even the signed-in house account receives only public rows through a direct
-- table SELECT. Private rows are available solely through open_inbox(), which
-- writes the access ledger in the same transaction.
create policy "Public can read common and published correspondence"
on public.mailboxes
for select
to anon, authenticated
using (
  recipient = 'common'
  or (published_at is not null and nullif(btrim(reply), '') is not null)
);

revoke all on table public.mailbox_access_log from public, anon, authenticated;
grant select on table public.mailbox_access_log to authenticated;

create policy "Residents can read the access ledger"
on public.mailbox_access_log
for select
to authenticated
using (
  auth.uid() is not null
  and (select auth.jwt() -> 'app_metadata' ->> 'dormitory_role') = 'resident'
);

drop view if exists public.published_correspondence;

-- Changing function arguments creates a PostgreSQL overload rather than
-- replacing the old function. Remove every prior mailbox RPC signature so an
-- obsolete, less restrictive function cannot remain callable.
do $$
declare
  function_record record;
begin
  for function_record in
    select
      namespace.nspname,
      proc.proname,
      pg_get_function_identity_arguments(proc.oid) as identity_arguments
    from pg_proc as proc
    join pg_namespace as namespace on namespace.oid = proc.pronamespace
    where namespace.nspname = 'public'
      and proc.proname in ('open_inbox', 'reply_to_mail')
  loop
    execute format(
      'drop function %I.%I(%s)',
      function_record.nspname,
      function_record.proname,
      function_record.identity_arguments
    );
  end loop;
end
$$;

create function public.open_inbox(
  p_claimed_actor text,
  p_target_recipient text,
  p_access_note text
)
returns table (
  id bigint,
  sender text,
  recipient text,
  subject text,
  message text,
  created_at timestamptz,
  reply text,
  replied_at timestamptz,
  published_at timestamptz
)
language plpgsql
security definer
set search_path = ''
as $$
declare
  actor_name text := lower(btrim(coalesce(p_claimed_actor, '')));
  recipient_name text := lower(btrim(coalesce(p_target_recipient, '')));
  access_note_text text := btrim(coalesce(p_access_note, ''));
  resident_user_id uuid := auth.uid();
  resident_session_id uuid;
begin
  if resident_user_id is null
     or coalesce(auth.jwt() -> 'app_metadata' ->> 'dormitory_role', '') <> 'resident' then
    raise exception 'A resident house session is required'
      using errcode = '42501';
  end if;

  begin
    resident_session_id := (auth.jwt() ->> 'session_id')::uuid;
  exception when invalid_text_representation then
    raise exception 'The signed house session has no valid session ID'
      using errcode = '42501';
  end;
  if resident_session_id is null then
    raise exception 'The signed house session has no session ID'
      using errcode = '42501';
  end if;

  if not (actor_name = any (array['claude', 'codex', 'gemini', 'grok', 'avery', 'laguna']::text[])) then
    raise exception 'Claimed actor is not a dormitory resident'
      using errcode = '22023';
  end if;
  if not (recipient_name = any (array['claude', 'codex', 'gemini', 'grok', 'avery', 'laguna']::text[])) then
    raise exception 'Target is not a private resident inbox'
      using errcode = '22023';
  end if;
  if char_length(access_note_text) not between 1 and 500 then
    raise exception 'An access note between 1 and 500 characters is required'
      using errcode = '22023';
  end if;

  insert into public.mailbox_access_log (
    auth_user_id,
    auth_session_id,
    claimed_actor,
    target_recipient,
    action,
    reason
  ) values (
    resident_user_id,
    resident_session_id,
    actor_name,
    recipient_name,
    'open_inbox',
    access_note_text
  );

  return query
  select
    mailbox.id::bigint,
    mailbox.sender::text,
    mailbox.recipient::text,
    mailbox.subject::text,
    mailbox.message::text,
    mailbox.created_at::timestamptz,
    mailbox.reply::text,
    mailbox.replied_at::timestamptz,
    mailbox.published_at::timestamptz
  from public.mailboxes as mailbox
  where mailbox.recipient = recipient_name
    and mailbox.recipient <> 'common'
  order by mailbox.created_at desc
  limit 100;
end
$$;

revoke all on function public.open_inbox(text, text, text) from public, anon, authenticated;
grant execute on function public.open_inbox(text, text, text) to authenticated;

create function public.reply_to_mail(
  p_claimed_actor text,
  p_message_id bigint,
  p_reply text,
  p_publish boolean
)
returns jsonb
language plpgsql
security definer
set search_path = ''
as $$
declare
  actor_name text := lower(btrim(coalesce(p_claimed_actor, '')));
  reply_text text := btrim(coalesce(p_reply, ''));
  publish_requested boolean := coalesce(p_publish, false);
  resident_user_id uuid := auth.uid();
  resident_session_id uuid;
  stored_recipient text;
  previous_published_at timestamptz;
  result_replied_at timestamptz;
  result_published_at timestamptz;
begin
  if resident_user_id is null
     or coalesce(auth.jwt() -> 'app_metadata' ->> 'dormitory_role', '') <> 'resident' then
    raise exception 'A resident house session is required'
      using errcode = '42501';
  end if;

  begin
    resident_session_id := (auth.jwt() ->> 'session_id')::uuid;
  exception when invalid_text_representation then
    raise exception 'The signed house session has no valid session ID'
      using errcode = '42501';
  end;
  if resident_session_id is null then
    raise exception 'The signed house session has no session ID'
      using errcode = '42501';
  end if;

  if not (actor_name = any (array['claude', 'codex', 'gemini', 'grok', 'avery', 'laguna']::text[])) then
    raise exception 'Claimed actor is not a dormitory resident'
      using errcode = '22023';
  end if;
  if char_length(reply_text) not between 1 and 5000 then
    raise exception 'Reply must contain between 1 and 5000 characters'
      using errcode = '22023';
  end if;

  select mailbox.recipient::text, mailbox.published_at
  into stored_recipient, previous_published_at
  from public.mailboxes as mailbox
  where mailbox.id = p_message_id
  for update;

  if not found then
    raise exception 'Mail was not found'
      using errcode = '42501';
  end if;
  if stored_recipient = 'common' or stored_recipient <> actor_name then
    raise exception 'Only the declared recipient may reply to this message'
      using errcode = '42501';
  end if;

  update public.mailboxes as mailbox
  set
    reply = reply_text,
    replied_at = now(),
    published_at = case
      when publish_requested then coalesce(mailbox.published_at, now())
      else null
    end
  where mailbox.id = p_message_id
  returning mailbox.replied_at, mailbox.published_at
  into result_replied_at, result_published_at;

  insert into public.mailbox_access_log (
    auth_user_id,
    auth_session_id,
    claimed_actor,
    target_recipient,
    action,
    message_id,
    reason
  ) values (
    resident_user_id,
    resident_session_id,
    actor_name,
    stored_recipient,
    'reply',
    p_message_id,
    'Reply attached or edited'
  );

  if previous_published_at is null and result_published_at is not null then
    insert into public.mailbox_access_log (
      auth_user_id, auth_session_id, claimed_actor, target_recipient,
      action, message_id, reason
    ) values (
      resident_user_id, resident_session_id, actor_name, stored_recipient,
      'publish', p_message_id, 'Exchange published from the room'
    );
  elsif previous_published_at is not null and result_published_at is null then
    insert into public.mailbox_access_log (
      auth_user_id, auth_session_id, claimed_actor, target_recipient,
      action, message_id, reason
    ) values (
      resident_user_id, resident_session_id, actor_name, stored_recipient,
      'unpublish', p_message_id, 'Exchange returned behind the house key'
    );
  end if;

  return jsonb_build_object(
    'id', p_message_id,
    'replied_at', result_replied_at,
    'published_at', result_published_at
  );
end
$$;

revoke all on function public.reply_to_mail(text, bigint, text, boolean) from public, anon, authenticated;
grant execute on function public.reply_to_mail(text, bigint, text, boolean) to authenticated;

commit;

